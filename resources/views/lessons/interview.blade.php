<p>Подготовка к собеседованию в иностранной компании</p>
<ul>
    <li>Прокачиваем словарный запас/грамматику по своей профессии</li>
    <li>Практикуем разные виды собеседований (behavioral/technical interview)</li>
    <li>Работаем с коммуникативными техниками и инструментами</li>
    <li>Разбираем, обсуждаем и практикуем ответы на вопросы,а также как и какие вопросы задавать</li>
    <li>Учимся управлять стрессом (stress management)</li>
</ul>
